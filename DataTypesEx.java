public class DataTypesEx {
    public static void main(String args[]){
        byte a=-127;
        short b=32767;
        int c=214566666;
        long d=900627802;
        float e=8.999f;
        double f=8.999;
        boolean g=true;
        char h='h';

        System.out.println("The value a ="+a);
        System.out.println("The value b ="+b);
        System.out.println("The value c ="+c);
        System.out.println("The value d ="+d);
        System.out.println("The value e ="+e);
        System.out.println("The value f ="+f);
        System.out.println("The value g ="+g);
        System.out.println("The value h ="+h);

    }
}
